# EXEMPLO - TUPLE()

#1. Crie uma tupla com os dias da semana
dias = ("Segunda-feira", "Tercera-feria", "Cuarta-feira", "Quinta-feira", "Sexta-feira", "Sábado", "Domingo")

#2. Utilize print() para exibir os dias da semana na tela
print("Los días de la semana son:", dias)

#3. Utilize o índice para exibir o primerio dia da semana
print("El primer día de la semana es:", dias[0])

#4. Utilize o índice para exibir o último dia da lista
print("El último día de la semana es:", dias[-1])

#5. Verifique o tamanho da tupla utilizando len(dias)
print("Tamanho da tupla 'dias':", len(dias))